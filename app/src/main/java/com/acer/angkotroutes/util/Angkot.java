package com.acer.angkotroutes.util;

public class Angkot {

    private String namaAngkot;
    private String namaGambar;

    public Angkot(String namaAngkot, String namaGambar) {
        this.namaAngkot = namaAngkot;
        this.namaGambar = namaGambar;
    }

    public String getNamaAngkot() {
        return namaAngkot;
    }

    public String getNamaGambar() {
        return namaGambar;
    }
}
